    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try{
            inputanKosongException();
            String role = "";

            role = groupRole.getSelection().getActionCommand();

            if(action.equals("Tambah")) {
                User d = new User(Integer.parseInt(idInput.getText()), namaInput.getText(), role, userInput.getText(), passInput.getText());
                userControl.insertDataUser(d);
            } else {
                User d = new User(selectedId, namaInput.getText(), role, userInput.getText(), passInput.getText());
                userControl.updateDataUser(d);
            }
            clearText();
            showUser();
            setComponent(false);
            setRadio(false);
            groupRole.clearSelection();
            setEditDeleteBtn(false);
        } catch(InputanKosongException e){
            JOptionPane.showMessageDialog(this, e.message());
        }
    }//GEN-LAST:event_saveBtnActionPerformed

