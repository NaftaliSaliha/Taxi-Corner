    private void tableUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableUserMouseClicked
        setEditDeleteBtn(true);
        setComponent(false);
        setRadio(false);
        groupRole.clearSelection();

        int clickedRow = tableUser.getSelectedRow();
        TableModel tableModel = tableUser.getModel();

        selectedId = Integer.parseInt(tableModel.getValueAt(clickedRow, 0).toString());
        idInput.setText(tableModel.getValueAt(clickedRow, 0).toString());
        namaInput.setText(tableModel.getValueAt(clickedRow, 1).toString());
        String role = tableModel.getValueAt(clickedRow, 2).toString();
        switch(role){
            case "Owner":
            ownerRadio.setSelected(true);
            break;
            case "Admin":
            adminRadio.setSelected(true);
            break;
        }
        userInput.setText(tableModel.getValueAt(clickedRow, 3).toString());
        passInput.setText(tableModel.getValueAt(clickedRow, 4).toString());
    }//GEN-LAST:event_tableUserMouseClicked

