    private void tablePenggunaanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablePenggunaanMouseClicked
        int indexDriver = -1;
        int indexTaksi = -1;
        setEditDeleteBtn(true);

        setComponent(false);

        int clickedRow = tablePenggunaan.getSelectedRow();
        TableModel tableModel = tablePenggunaan.getModel();

        selectedId = Integer.parseInt(tableModel.getValueAt(clickedRow, 6).toString());
        penumpangInput.setText(tableModel.getValueAt(clickedRow, 2).toString());
        tarifInput.setText(tableModel.getValueAt(clickedRow, 3).toString());
        tanggalInput.setText(tableModel.getValueAt(clickedRow, 4).toString());
        layananInput.setText(tableModel.getValueAt(clickedRow, 5).toString());

        String id_driver = tableModel.getValueAt(clickedRow, 7).toString();
        for(Driver driver : listDriver){
            if(String.valueOf(driver.getId()).equals(id_driver)){
                indexDriver = listDriver.indexOf(driver);
            }
        }

        driverDropdown.setSelectedIndex(indexDriver);

        String id_taksi = tableModel.getValueAt(clickedRow, 8).toString();
        for(Taksi taksi : listTaksi){
            if(String.valueOf(taksi.getId()).equals(id_taksi)) {
                indexTaksi = listTaksi.indexOf(taksi);
            }
        }

        taksiDropdown.setSelectedIndex(indexTaksi);
    }//GEN-LAST:event_tablePenggunaanMouseClicked

