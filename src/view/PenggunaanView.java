    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        int getAnswer = JOptionPane.showConfirmDialog(rootPane, "Apakah yakin ingin menghapus data?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        switch(getAnswer){
            case 0:
            try{
                penggunaanControl.deletePenggunaan(selectedId);
                clearText();
                showPenggunaan();
                setComponent(false);
                setEditDeleteBtn(false);
            } catch(Exception e){
                System.out.println("Error : " + e.getMessage());
            }
            break;
            case 1:
            break;
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try{
            inputanKosongException();
            String fasilitas = "";
            int selectedIndexD = driverDropdown.getSelectedIndex();
            Driver selectedDriver = listDriver.get(selectedIndexD);
                int selectedIndexT = taksiDropdown.getSelectedIndex();
                Taksi selectedTaksi = listTaksi.get(selectedIndexT);

                if(action.equals("Tambah")){
                    Penggunaan p = new Penggunaan(tanggalInput.getText(),
                        layananInput.getText(), penumpangInput.getText(), Integer.parseInt(tarifInput.getText()), selectedDriver, selectedTaksi);
                    penggunaanControl.insertPenggunaan(p);
                } else{
                    Penggunaan p = new Penggunaan(selectedId, tanggalInput.getText(),
                        layananInput.getText(), penumpangInput.getText(), Integer.parseInt(tarifInput.getText()), selectedDriver, selectedTaksi);
                    penggunaanControl.updatePenggunaan(p);
                }
                clearText();
                showPenggunaan();
                setComponent(false);

                setEditDeleteBtn(false);
        } catch(InputanKosongException e){
            JOptionPane.showMessageDialog(this, e.message());
        }
    }//GEN-LAST:event_saveBtnActionPerformed

