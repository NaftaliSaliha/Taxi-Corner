    public void setDriverToDropdown(){
        listDriver = driverControl.showListDriver();
        for(int i = 0; i < listDriver.size(); i++){
            driverDropdown.addItem(listDriver.get(i));
        }
    }
    
    public void setTaksiToDropdown(){
        listTaksi = taksiControl.showListTaksi();
        for(int i = 0; i < listTaksi.size(); i++){
            taksiDropdown.addItem(listTaksi.get(i));
        }
    }
    
